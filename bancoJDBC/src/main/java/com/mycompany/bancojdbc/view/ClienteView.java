    private void salvarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBtnActionPerformed
        try {
            String nome = nomeTxt.getText();
            String sobrenome = sobreNomeTxt.getText();
            String cpf = cpfTxt.getText().replaceAll("[.-]", "");
            String rg = rgTxt.getText().replaceAll("[.-]", "");
            String endereco = enderecoTxt.getText();
            String salario = salarioTxt.getText();
                      
                if(nome.isBlank() || sobrenome.isBlank() || cpf.isBlank() || rg.isBlank() || endereco.isBlank() || salario.isBlank()){
                    JOptionPane.showMessageDialog(null,"Preencha todos os campos!");
                } else {
                    Cliente c = new Cliente(nome,sobrenome,cpf,rg,endereco,Double.valueOf(salario));
                    ClienteController.salvarCliente(c);    
                    limparCampos();
                    System.out.println("Cliente no salvarBtn():" + c.toString());
                }           
            atualizaLista();
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Insira um valor de salário válido!");
        } catch (SQLException ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_salvarBtnActionPerformed

    private void jTableClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesMouseClicked
        int index = jTableClientes.getSelectedRow();
        TableModel model = jTableClientes.getModel();
        
        nomeTxt.setText(model.getValueAt(index, 0).toString());
        sobreNomeTxt.setText(model.getValueAt(index, 1).toString());
        cpfTxt.setText(model.getValueAt(index, 2).toString());
        rgTxt.setText(model.getValueAt(index, 3).toString());
        enderecoTxt.setText(model.getValueAt(index, 4).toString());
        salarioTxt.setText(model.getValueAt(index, 5).toString());
    }//GEN-LAST:event_jTableClientesMouseClicked

    private void excluiBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluiBtnActionPerformed
        Cliente cliente = getCliente();
        try {
            if(!cpfTxt.getText().trim().strip().isBlank()) {

                    if(cliente != null) {
                        int reply = JOptionPane.showConfirmDialog(null,"Deseja realmente excluir este cliente?\n(Todas as contas vinculadas a este cliente serao apagadas)", "Confirmação", JOptionPane.YES_OPTION);
                        if(reply == JOptionPane.YES_OPTION)
                            ClienteController.excluirCliente(cliente.getCPF());
                    } else {  
                        JOptionPane.showMessageDialog(null,"O CPF informado não corresponde a nenhum Cliente no banco!");
                    }

            }else {
                JOptionPane.showMessageDialog(null,"Selecione um cliente a ser excluido!");
            }
            atualizaLista();
            limparCampos();
        } catch (SQLException ex) {
                Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_excluiBtnActionPerformed

